class Shape:
    def draw(s):
        print("Drawing a shape")
class Circle(Shape):
    def draw(Shape):
        print("Drawing a Circle")
class Square(Shape):
    def draw(s):
        print("Drawing a Square")
class Triangle(Shape):
    def draw(s):
        print("Drawing a Triangle")
obj = Triangle()
obj.draw()
obj = Circle()
obj.draw()
obj = Square()
obj.draw()
obj = Shape()
obj.draw()
